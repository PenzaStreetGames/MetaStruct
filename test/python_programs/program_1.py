# Вычислить результат преобразования числа x = 42 по алгоритму:
# 1. Прибавить 80, целочисленно разделить на 2 (61)
# 2. Вычесть 34, умножить на 7 (189)
# 3. Получить остаток от деления на 103 (86)
# 4. Сдвинуть число на 2 бит влево и выполнить побитовое И с числом 843 (328)
# 5. Выполнить побитовое ИЛИ с числом 55, сдвинуть влево на 3 бита (47)
# 6. Возвести результат в квадрат минус текущее значение (2162)
# 7. Получить остаток от деления числа на 68 (54)
# 8. Записать результат в переменную y


def main() -> int:
    x: int = 42
    x = (x + 80) // 2
    x = (x - 34) * 7
    x = x % 103
    x = (x << 2) & 843
    x = (x | 55) >> 3
    x = x * x - x
    x = x % 68
    y: int = x
    return y
